def skeleton_normalize(reason: str) -> str:
    """骨架归一（v8 激进删除模式）：自动识别 reason 文本中的"变量 token"（订单号/账号ID/金额/时间戳/乘客姓名等），
    直接删除（替换为空字符串）。相同"业务文本字样"的不同变体会被自动归并成同一条。

    优点：
      - 新 reason 进来时不用写新规则，骨架归一器自动处理
      - 避免每种变体（订单号不同、账号不同）都拆成单独一行
      - 把"业务骨架"暴露出来，更容易做族级聚合
      - 不留占位符，看着干净
    """
    if not reason:
        return ""
    s = reason
    for pattern, replacement in SKELETON_NORMALIZE_RULES:
        s = re.sub(pattern, replacement, s)
    # 清理被删后残留的孤立符号（开头/结尾的 - : , . 空格 + 孤立引号）
    s = re.sub(r'^[\s\-_:：,，。."\']+', "", s)
    s = re.sub(r'[\s\-_:：,，。."\']+$', "", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s


def get_root_reason(row) -> str:
    """获取清洗后的根因文本。

    优先级：
      1) 第一次失败原因 字段（清洗后）
      2) fallback 到 失败原因 字段（清洗后）
      3) 仍然为空 → 返回 ""
    """
    r1 = clean_reason_text(row.get("第一次失败原因", ""))
    if r1:
        return r1
    r2 = clean_reason_text(row.get("失败原因", ""))
    return r2


